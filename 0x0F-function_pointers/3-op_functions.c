#include "3-calc.h"

/**
 * op_add - add 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: division result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculate remainder of division of 2 numbers
 * @a: 1st number
 * @b: 2nd number
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
